Who are we? <br>
<h4> A designated environment for Regis students to post about or rate various aspects of the Regis student experience
Regians often have many opinions about what the best cafeteria food, resource center, or varsity sport our school has to offer. 
Our application provides a space for students of all grade levels to voice their ideas, building knowledge for future classes to learn from in years to come. <br> <br>
Finding others with similar views can build bonds within the student body and help underclassmen make friends or get acclimated to their new high school environment. 
This project can even extend to Regis faculty as an alternate way to connect with their students outside of Moodle in a unique space where all are on an equal level, adding a new facet of the relationship between student and teacher. <br> <br>
Users will be able to access many features that can positively enhance their often stressful school experience through the posts and insights of others. 
Administrators, meanwhile, can moderate posts by removing or delisting posts that are not appropriate for the school setting, establishing forum rules and regulations by which all users must abide. 
Permissions can be granted and removed by the moderators, who will oversee all the posts, images, and ratings uploaded to the site. </h4>

